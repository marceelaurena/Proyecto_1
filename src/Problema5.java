
public class Problema5 {
    public static void main(String[] args) {
    // Problema 5 Valor de PI
double pi=4;
boolean resta = true;
for (int x = 3; x <= 101; x+=2) {
    
if (resta = true) {
    resta = false;
    pi = pi - (4/x);
} 
else{
    resta = true;
    pi = pi + (4/x);
}
System.out.println("El valor de PI es: " + pi);
    }}}

